    private void crearNuevoEstudiante(){
        
        estudiante = new Estudiantes();
        estudiante.setNombre(txtnombre.getText().trim());
        estudiante.setDni(txtdni.getText().trim());
        estudiante.setGradoestudiante(txtgrado.getText());
        estudiante.setColegio(txtcolegio.getText());
        
       
        this.padre.agregarNuevoEstudiante(estudiante);
        
    }
    
    
    
    
    
    
    
