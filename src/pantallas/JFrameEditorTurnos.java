    private void crearNuevoTurno(){
        
        turno = new Turnos();
        turno.setTurnoclase(txtturnoclase.getText());
        turno.setCantidadalumnos(txtcantidad.getText());
        turno.setProfesor(txtprofesor.getText());
        turno.setDuracion(txtduracion.getText());
        turno.setCurso(txtcurso.getText());
        turno.setTemacurso(txttema.getText());
        
        
        this.padre.agregarNuevoTurno(turno);
        
    }
    
    private void salir(){
        padre.setVisible(true);
        this.dispose();
    }
    
    
    
