    private void crearNuevoAsistencia(){
        
        asistencia = new Asistencia();
        asistencia.setFecha(txtfecha.getText());
        asistencia.setTurnoclase(txtturnoclase.getText());
        asistencia.setDuracion(txtduracion.getText());
        asistencia.setAsistieron(txtasisteron.getText());
        asistencia.setFaltaron(txtfaltaron.getText());
        
        
        this.padre.agregarNuevoAsistencia(asistencia);
        
    }
    
